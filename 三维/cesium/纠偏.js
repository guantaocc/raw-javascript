// （1）WGS84
// 即GPS原始坐标系，也是当今三维开发中最普遍使用的坐标系。
// （2）GCJ-02：火星坐标系
// 由国测局发布的加密坐标系，是国内互联网地图最广泛使用的坐标系，包括高德、腾讯、谷歌中国地图。
// （3）BD-09
// 百度地图所采用的坐标系，即在火星坐标系上进一步偏移得到。
// （4）CGCS2000
// 又称2000国家大地坐标系，由西安80坐标系发展而来，一般使用高斯克吕格投影，用于国内土地调查等坐标参考系。天地图采用此坐标系。
